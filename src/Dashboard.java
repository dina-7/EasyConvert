    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        // TODO add your handling code here:
            String fromCurrency = (String) jComboBox11.getSelectedItem();
        String toCurrency = (String) jComboBox12.getSelectedItem();

        double inputValue;
        try {
            inputValue = Double.parseDouble(jTextField11.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Enter a valid numeric value.");
            return;
        }

        double result = inputValue * (getExchangeRate(toCurrency) / getExchangeRate(fromCurrency));
        jTextField12.setText(String.format("%.2f", result));
    }

    private double getExchangeRate(String currency) {
        switch (currency) {
            case "USD":
                return usdRate;
            case "CHF":
                return chfRate;
            case "BAM":
                return bamRate;
            case "HRK":
                return hrkRate;
            case "RSD":
                return rsdRate;
            case "CustomCurrency":
                return customRate;
            default:
                return eurRate;
        }
    }//GEN-LAST:event_jButton22ActionPerformed

